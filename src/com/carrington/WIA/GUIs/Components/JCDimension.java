package com.carrington.WIA.GUIs.Components;

public class JCDimension {
	
	private final int height;
	private final int width;
	
	public JCDimension(int height, int width) {
		this.height = height;
		this.width = width;
	}
	
	public int getHeight() {
		return this.height;
	}
	
	public int getWidth() {
		return this.width;
	}
	
}
